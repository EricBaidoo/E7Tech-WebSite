// Partners Carousel Auto-Scroll
(function() {
    var carousel = document.getElementById('partnersCarousel');
    if (!carousel) return;
    
    var currentTranslate = 0;
    var itemWidth = 25; // 25% for 4 items visible
    var totalSlides = 11;
    var maxTranslate = -(itemWidth * (totalSlides - 4)); // Stop when last 4 are visible
    
    function autoScroll() {
        currentTranslate -= itemWidth;
        
        // Reset to beginning when we've scrolled through all
        if (currentTranslate <= maxTranslate) {
            currentTranslate = 0;
        }
        
        carousel.style.transform = 'translateX(' + currentTranslate + '%)';
    }
    
    // Auto-scroll every 3 seconds
    setInterval(autoScroll, 3000);
})();
</script>

