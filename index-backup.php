    <!-- Hero Slider Script -->
    <script>
        document.addEventListener('DOMContentLoaded', function() {
            // ===== HERO SLIDER =====
            const slides = document.querySelectorAll('.hero-slide');
            const dots = document.querySelectorAll('.slider-dots .dot');
            let currentSlide = 0;
            let slideInterval;

            function showSlide(index) {
                slides.forEach(slide => slide.classList.remove('active'));
                dots.forEach(dot => dot.classList.remove('active'));

                if (index >= slides.length) {
                    currentSlide = 0;
                } else if (index < 0) {
                    currentSlide = slides.length - 1;
                } else {
                    currentSlide = index;
                }

                slides[currentSlide].classList.add('active');
                dots[currentSlide].classList.add('active');
            }

            function nextSlide() {
                showSlide(currentSlide + 1);
            }

            function startAutoPlay() {
                slideInterval = setInterval(nextSlide, 7000);
            }

            function stopAutoPlay() {
                clearInterval(slideInterval);
            }

            dots.forEach((dot, index) => {
                dot.addEventListener('click', () => {
                    showSlide(index);
                    stopAutoPlay();
                    startAutoPlay();
                });
            });

            const heroSlider = document.querySelector('.home-hero-slider');
            heroSlider.addEventListener('mouseenter', stopAutoPlay);
            heroSlider.addEventListener('mouseleave', startAutoPlay);

            startAutoPlay();

            // ===== STATS COUNTER ANIMATION =====
            const stats = document.querySelectorAll('.stat-number');
            let statsAnimated = false;

            function animateStats() {
                stats.forEach(stat => {
                    const target = parseInt(stat.getAttribute('data-target'));
                    const duration = 2000;
                    const increment = target / (duration / 16);
                    let current = 0;

                    const updateCounter = () => {
                        current += increment;
                        if (current < target) {
                            stat.textContent = Math.floor(current);
                            requestAnimationFrame(updateCounter);
                        } else {
                            stat.textContent = target;
                        }
                    };
                    updateCounter();
                });
            }

            // ===== SCROLL ANIMATIONS =====
            const fadeInSections = document.querySelectorAll('.fade-in-section');
            
            const observerOptions = {
                threshold: 0.1,
                rootMargin: '0px 0px -50px 0px'
            };

            const sectionObserver = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('visible');
                        
                        // Trigger stats animation when stats section becomes visible
                        if (entry.target.classList.contains('stats-section') && !statsAnimated) {
                            animateStats();
                            statsAnimated = true;
                        }
                    }
                });
            }, observerOptions);

            fadeInSections.forEach(section => {
                sectionObserver.observe(section);
            });

            // Stats section observer
            const statsSection = document.querySelector('.stats-section');
            if (statsSection) {
                sectionObserver.observe(statsSection);
            }

            // ===== PARALLAX EFFECT =====
            let ticking = false;
            
            window.addEventListener('scroll', () => {
                if (!ticking) {
                    window.requestAnimationFrame(() => {
                        const scrolled = window.pageYOffset;
                        const heroSlider = document.querySelector('.home-hero-slider');
                        
                        if (heroSlider && scrolled < window.innerHeight) {
                            heroSlider.style.transform = `translateY(${scrolled * 0.5}px)`;
                        }
                        
                        ticking = false;
                    });
                    ticking = true;
                }
            });

            // ===== CARD HOVER EFFECTS =====
            const cards = document.querySelectorAll('.solution-card, .challenge-card, .why-card, .client-card');
            
            cards.forEach(card => {
                card.addEventListener('mouseenter', function() {
                    this.style.transform = 'translateY(-8px) scale(1.02)';
                });
                
                card.addEventListener('mouseleave', function() {
                    this.style.transform = 'translateY(0) scale(1)';
                });
            });
        });
    </script>
